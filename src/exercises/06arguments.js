// Functions can also accept arguments.
// Arguments are things the test will give to your function. They can be
// different every time. The test may call your function multiple times.
// Here is an example of a function with one argument:

function one(a) {
  return a
}

// Here's an example of a function with two arguments.

function two(a,b) {
  return a + b
}

// Notice that we can only return one thing, but we can accept as many arguments
// as we want.

